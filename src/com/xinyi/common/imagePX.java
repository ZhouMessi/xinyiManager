package com.xinyi.common;

//像素
public class imagePX {

	private int imgWide;
	private int imgHigh;
	public int getImgWide() {
		return imgWide;
	}
	public void setImgWide(int imgWide) {
		this.imgWide = imgWide;
	}
	public int getImgHigh() {
		return imgHigh;
	}
	public void setImgHigh(int imgHigh) {
		this.imgHigh = imgHigh;
	}
	public imagePX(int imgWide, int imgHigh) {
		super();
		this.imgWide = imgWide;
		this.imgHigh = imgHigh;
	}
	
	
}
